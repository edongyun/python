# 인덱싱하기
t1 = (1, 2, 'a', 'b')
print(t1)       # (1, 2, 'a', 'b')

# 슬라이싱하기
print(t1[1:])   # (2, 'a', 'b')

# 튜플 더하기
t1 = (1, 2, 'a', 'b')
t2 = (3, 4)
print(t1+t2)    # (1, 2, 'a', 'b', 3, 4)

# 튜플 곱하기
t2 = (3, 4)
print(t2*3)     # (3, 4, 3, 4, 3, 4)

# 튜플 길이 구하기
t1 = (1, 2, 'a', 'b')
print(len(t1))  # 4

